package ro.atek.qsparser.value;

/**
 * A mean of easy retrieval of data type for the underlying value.
 */
public enum ValueType
{
   /** Null value */
   NULL,
   /** Boolean value */
   BOOL,
   /** Integer value */
   INT,
   /** String value */
   STRING,
   /** Array value */
   ARRAY,
   /** Dictionary value */
   DICT
}
